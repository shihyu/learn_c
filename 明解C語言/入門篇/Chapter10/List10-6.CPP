#include  <stdio.h>

/*---nxPny飒邯ンぇ趣传---*/
void swap(int& nx, int& ny)
{
     int  temp = nx;
     nx = ny;
     ny = temp;
}

int main(void)
{
    int	 na, nb;

    puts("叫块Jㄢ泳慵啤G");
    printf("俱计AG");       scanf("%d", &na);
    printf("俱计BG");       scanf("%d", &nb);

    swap(na, nb);

    puts("ㄢ去娲帷G");
    printf("俱计A%dC\n", na);
    printf("俱计B%dC\n", nb);
    
    return (0);
}
